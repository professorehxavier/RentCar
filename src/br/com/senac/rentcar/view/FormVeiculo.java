    private boolean validarCampos() {
        if (txtChassi.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Chassi é obrigatório!");
            return false;
        } else if (txtPlaca.getText().replace("-", "").trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Placa é obrigatório!");
            return false;
        } else if (txtMarca.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Marca é obrigatório!");
            return false;
        } else if (txtModelo.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Modelo é obrigatório!");
            return false;
        } else if (txtAnoFabricacao.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Ano de Fabricação obrigatório!");
            return false;
        } else if (txtValorLocacao.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo Valor de Locação obrigatório!");
            return false;
        } 
        return true;
    }

