    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        if (validarCampos()) {
            veiculo.setChassi(txtChassi.getText().trim());
            veiculo.setPlaca(txtPlaca.getText().trim());
            veiculo.setMarca(txtMarca.getText().trim());
            veiculo.setModelo(txtModelo.getText().trim());
            veiculo.setAnoFabricacao(Integer.parseInt(txtAnoFabricacao.getText().trim()));
            veiculo.setValorLocacao(Double.parseDouble(txtValorLocacao.getText().trim()));
            veiculo.setStatus((String) comboStatus.getSelectedItem());

            //Salvar no banco de dados
            VeiculoDAO veiculoDAO = new VeiculoDAO();
            boolean resultado;
            
            if(this.alterar == true){
                resultado = veiculoDAO.alterarVeiculo(veiculo);
                this.formListagem.atualizar();
            }else{
                resultado = veiculoDAO.inserirVeiculo(veiculo);
            }
            
            if (resultado) {
                JOptionPane.showMessageDialog(null, "Veículo salvo com sucesso!");
                this.dispose();
            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

